    private static int[] reverseArray(int[] array) {
        int i, temp;
        int size = array.length;
        for (i = 0; i < size / 2; i++) {
            temp = array[i];
            array[i] = array[size - i - 1];
            array[size - i - 1] = temp;
        }
        return array;
    }

    private static int[] add(int[] num1, int[] num2) {
        var locNum1 = Arrays.copyOf(num1, num1.length);
        var locNum2 = Arrays.copyOf(num2, num2.length);
        //finding larger array, num2 must be greater
        if (locNum1.length > locNum2.length) {
            int[] temp = locNum1;
            locNum1 = locNum2;
            locNum2 = temp;
        }

        //array for result
        int[] result = new int[locNum1.length + locNum2.length + 1];

        //reversing arrays
        reverseArray(locNum1);
        reverseArray(locNum2);

        //do the adding
        int next = 0;
        for (int i = 0; i < locNum1.length; i++) {
            int sum = locNum1[i] + locNum2[i] + next;
            result[i] = sum % 10;
            next = sum / 10;
        }

        //take care of rest of num2
        int i = locNum1.length;
        for (; i < locNum2.length; i++) {
            int sum = locNum2[i] + next;
            result[i] = sum % 10;
            next = sum / 10;
        }

        //add next if there is one
        if (next > 0) {
            result[i] = next;
            i++;
        }

        //reverse cut array and return
        result = reverseArray(Arrays.copyOfRange(result, 0, i));
        return result;
    }

    private static int[] multiply(int[] num1, int[] num2) {
        var locNum1 = Arrays.copyOf(num1, num1.length);
        var locNum2 = Arrays.copyOf(num2, num2.length);

        int len1 = locNum1.length;
        int len2 = locNum2.length;

        if ((len1 == 1 && locNum1[0] == 0) || (len2 == 1 && locNum2[0] == 0)) return new int[]{0};

        //reversed result
        int[] result = new int[len1+len2];

        //indexes to navigate
        int id1 = 0;
        int id2;

        //calculations
        for (int i = len1 - 1; i >= 0; i--) {
            int next = 0;
            int n1 = locNum1[i];

            // To shift position to left after every
            id2 = 0;

            for (int j = len2 - 1; j >= 0; j--) {
                // Take current digit of second number
                int n2 = locNum2[j];

                //multiply add to the previous result and add next
                int sum = n1 * n2 + result[id1 + id2] + next;

                //next for next go
                next = sum / 10;

                // Store result
                result[id1 + id2] = sum % 10;

                id2++;
            }

            // store next in next cell
            if (next > 0)
                result[id1 + id2] += next;

            // To shift position to left after every go
            id1++;
        }

        // index to remove zeros
        int i = result.length - 1;
        while (i >= 0 && result[i] == 0)
            i--;

        return reverseArray(Arrays.copyOfRange(result, 0, i + 1));
    }

